package vorlesung.staticMethods;

class MyUtils {

	static double max(double parameter1, double parameter2) {
		if (parameter1 > parameter2) {
			return parameter1;
		} else {
			return parameter2;
		}
	}
	
	static int max(int parameter1, int parameter2) {
		if (parameter1 > parameter2) {
			return parameter1;
		} else {
			return parameter2;
		}
	}
	
}
